    public @NotNull NamespacedKey getKey() {
        //prefer block type id as Bukkit assigns some item types that dont have the same ID
        if (this.blockType != null) {
            ResourceKey key = this.asBlockType().key(RegistryTypes.BLOCK_TYPE);
            return SoakResourceKeyMap.mapToBukkit(key);
        }
        ResourceKey key = this.asItemType().key(RegistryTypes.ITEM_TYPE);
        return SoakResourceKeyMap.mapToBukkit(key);
    }

