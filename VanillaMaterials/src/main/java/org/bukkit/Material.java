    //uses minecraft namespace
    public static @Nullable Material matchMaterial(@NotNull String name) {
        name = name.toLowerCase();
        name = name.replaceAll(" ", "_");
        ResourceKey key = ResourceKey.resolve(name);
        try {
            return getMaterial(key);
        } catch (RuntimeException e) {
            return null;
        }
    }

    public static @Nullable Material getMaterial(ResourceKey key) {
        var opBlockType = BlockTypes.registry().findValue(key);
        if (opBlockType.isPresent()) {
            return getBlockMaterial(opBlockType.get());
        }

        var opItemType = ItemTypes.registry().findValue(key);
        if (opItemType.isPresent()) {
            return getItemMaterial(opItemType.get());
        }
        return null;
    }

    public static @Nullable Material getMaterial(@NotNull String name) {
        try {
            return valueOf(name);
        } catch (Exception e) {
            return null;
        }
    }

    public static @NotNull Material getBlockMaterial(BlockType type) {
        String plainName = type.key(RegistryTypes.BLOCK_TYPE).value();
        return Stream
                .of(values())
                .filter(Material::isBlock)
                .filter(mat -> mat.name().equalsIgnoreCase(plainName))
                .findFirst()
                .orElseThrow(() -> new RuntimeException("Could not find a material that matched the BlockType " + plainName));
    }

    public static @NotNull Material getBlockMaterial(Supplier<BlockType> type) {
        return getBlockMaterial(type.get());
    }

    public static @NotNull Material getItemMaterial(ItemType type) {
        String plainName = type.key(RegistryTypes.ITEM_TYPE).value();
        return Stream
                .of(values())
                .filter(Material::isItem)
                .filter(mat -> mat.name().equalsIgnoreCase(plainName))
                .findFirst()
                .orElseThrow(() -> new RuntimeException("Could not find a material that matched the ItemType " + plainName));
    }

    public static @NotNull Material getItemMaterial(Supplier<ItemType> type) {
        return getItemMaterial(type.get());
    }

